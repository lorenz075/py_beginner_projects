def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1/n2
 
def calculator():
    operations = {"+":add, "-":subtract, "*":multiply, "/":divide}

    num1 = float(input("Type the first number: "))
    for operation in operations:
        print(operation)
    should_continue = True  

    while should_continue:
        operation_symbol = input("Pick an operation: ")
        num2 = float(input("Type the next number: "))
        get_function = operations[operation_symbol]
        answer = round(get_function(num1, num2), 2)

        print(f"{num1} {operation_symbol} {num2} = {answer}")

        if input(f"Type 'y' to continue calculating with {answer}, or 'n' to start a new calculation: ") == "y":
            num1 = answer
        else:
            should_continue = False
            calculator()
            
calculator()